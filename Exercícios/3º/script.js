//Valor da variável.
let n = 10;

//Somando 5 e reatribuindo o valor.
n += 5;

//Multiplicando por 5 e reatribuindo o valor.
n *= 5;

//Somando 1 e reatribuindo o valor.
n++;

//Exibindo o resto da divisão por 6.
alert("O resto da divisão de " + n + " por 6 é " + (n % 6));

//Exibindo o resto da divisão por 6 elevado ao quadrado.
alert("O quadrado do resto da divisao de " + n + " por 6 é " + ( (n % 6) ** 2));

//Exibindo o valor da variável.
alert(n);

//Exibindo na tela o valor dividio por 4 e reatribuindo.
alert(n /= 4);