//Guardando os valores do usuário.
let n1 = parseFloat(prompt("Digite um número entre 0 e 10: "));
let n2 = parseFloat(prompt("Digite o segundo número entre 0 e 10: "));

//Checando se o segundo número é maior ou igual o primeiro.
if (n2 === n1 || n2 > n1) {

    //Exibindo.  
    alert(true);
} else {

    //Exibindo.
    alert(false);
}